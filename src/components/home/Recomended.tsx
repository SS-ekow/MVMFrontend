export function FeaturedProducts() {
  const [currentPage, setCurrentPage] = useState(0)

  const nextPage = () => {
    setCurrentPage((prev) => (prev + 1) % Math.ceil(products.length / 4))
  }

  const prevPage = () => {
    setCurrentPage((prev) => (prev - 1 + Math.ceil(products.length / 4)) % Math.ceil(products.length / 4))
  }

  return (
    <section className="py-16">
      <div className="container mx-auto px-4">
        <div className="mb-8 flex items-center justify-between">
          <h2 className="text-2xl font-bold text-[#272343]">Featured Products</h2>
          <div className="flex gap-2">
            <Button onClick={prevPage} variant="outlined" size="small" className="h-10 w-10 rounded-full border-gray-200">
              <ArrowLeft className="h-4 w-4" />
            </Button>
            <Button onClick={nextPage} variant="outlined" size="small" className="h-10 w-10 rounded-full border-gray-200">
              <ArrowRight className="h-4 w-4" />
            </Button>
          </div>
        </div>

        <div className="grid grid-cols-1 gap-6 sm:grid-cols-2 lg:grid-cols-4">
          {products.slice(currentPage * 4, (currentPage + 1) * 4).map((product) => (
            <ProductCard
              key={product.id}
              title={product.title}
              price={product.price}
              originalPrice={product.originalPrice}
              image={product.image}
              isNew={product.isNew}
              isSale={product.isSale}
              onAddToCart={() => console.log("Added to cart:", product.id)}
              onAddToWishlist={() => console.log("Added to wishlist:", product.id)}
            />
          ))}
        </div>
      </div>
    </section>
  )
}

